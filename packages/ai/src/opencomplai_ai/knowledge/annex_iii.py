def lookup_by_area(area: int) -> list[AnnexIIIEntry]:
    return [e for e in ANNEX_III if e.area == area]


def lookup_by_code_signal(token: str) -> list[AnnexIIIEntry]:
    from opencomplai_core.scanner.detectors._signals import match_code_signal

    return [
        e
        for e in ANNEX_III
        if any(match_code_signal(token, sig) for sig in e.code_signals)
    ]


def lookup_by_keyword(text: str) -> list[AnnexIIIEntry]:
    text_lower = text.lower()
    return [e for e in ANNEX_III if any(kw in text_lower for kw in e.keywords)]


def all_code_signals() -> set[str]:
    result: set[str] = set()
    for entry in ANNEX_III:
        result.update(entry.code_signals)
    return result


def all_keywords() -> set[str]:
    result: set[str] = set()
