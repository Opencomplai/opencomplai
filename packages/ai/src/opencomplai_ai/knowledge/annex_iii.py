    area: int
    area_name: str
    subpoint: str
    title: str
    obligation_articles: tuple[str, ...]
    conformity_path: str  # "self_assessment" | "notified_body_optional"
    keywords: tuple[str, ...]
    code_signals: tuple[str, ...]
    excludes: tuple[str, ...]
    art6_3_profiling: bool = False
    # True when the regulation text scopes this sub-point to natural persons
    # specifically (e.g. "creditworthiness of natural persons", Annex III 5(b)).
    # For these entries a keyword/code-signal match alone is not sufficient —
    # the scanner must also find evidence the *subject being scored* is a
    # person, not a product, portfolio, or commercial counterparty. Areas that
    # regulate a system/context rather than a person-scoring decision (e.g.
    # biometrics, critical infrastructure, judicial research) are not gated.
    subject_gated: bool = False


